export const getPromptForSkill = (skill) => {
  const list = prompts[skill] || prompts.typing
  return list[Math.floor(Math.random() * list.length)]
}

export const getDailyPrompts = () => {
  const today = new Date().toDateString()
  const seed = today.split('').reduce((a, c) => a + c.charCodeAt(0), 0)
  const rand = (arr) => arr[(seed * 31 + arr.length) % arr.length]
  return {
    name: rand(prompts.names),
    address: rand(prompts.addresses),
    invoice: rand(prompts.invoices),
    tenKey: rand(prompts.tenKey),
    email: rand(prompts.emails),
  }
}
